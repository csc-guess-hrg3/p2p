IF CHARINDEX('CP',@TIPO_A_GERAR) > 0
	BEGIN
		INSERT #CTB_FLUXO_CAIXA_TEMP (DATA, CONTAS_A_PAGAR)
		SELECT  (CASE WHEN @VENCIMENTO_REAL = 1 THEN DBO.LX_DATA_REAL(NULL,A.VENCIMENTO_REAL+@D_CP) ELSE A.VENCIMENTO+@D_CP END), 
					SUM(B.VALOR_A_PAGAR_PADRAO)
		FROM CTB_A_PAGAR_PARCELA A
				JOIN W_CTB_A_PAGAR_PARCELA_SALDO B ON
					A.EMPRESA			= B.EMPRESA		AND
					A.LANCAMENTO		= B.LANCAMENTO	AND
					A.ITEM				= B.ITEM			AND
					A.ID_PARCELA		= B.ID_PARCELA
				JOIN CTB_LANCAMENTO C ON
					A.EMPRESA			= C.EMPRESA		AND
					A.LANCAMENTO		= C.LANCAMENTO
            JOIN CTB_A_PAGAR_FATURA D ON
               A.EMPRESA         = D.EMPRESA    AND
               A.LANCAMENTO      = D.LANCAMENTO AND
               A.ITEM            = D.ITEM
				JOIN CTB_LANCAMENTO_ITEM W ON 
					W.EMPRESA			= A.EMPRESA		AND
					W.LANCAMENTO		= A.LANCAMENTO	AND
					W.ITEM				= A.ITEM
				LEFT JOIN #RATEIO_FILIAL_MATRIZ_CONTABIL_TEMP Y ON
					W.RATEIO_FILIAL	= Y.RATEIO_FILIAL
				JOIN #USERS_MATRIZ_CONTABIL_TEMP Z ON
					Y.COD_MATRIZ_CONTABIL			= Z.COD_MATRIZ_CONTABIL
		WHERE (A.EMPRESA = @EMPRESA OR @EMPRESA IS NULL) AND --#01#
				CASE WHEN @VENCIMENTO_REAL = 1 THEN A.VENCIMENTO_REAL ELSE A.VENCIMENTO END 	>= @DATA_INICIAL-(CASE WHEN @ZERA_D <> 0 THEN @D_CP ELSE 0 END)	AND
				CASE WHEN @VENCIMENTO_REAL = 1 THEN A.VENCIMENTO_REAL ELSE A.VENCIMENTO END 	<= @DATA_FINAL				AND
				A.VALOR_A_PAGAR 				> 0	AND
				(C.TIPO_MOVIMENTO 		= @TIPO_MOVIMENTO 		OR @TIPO_MOVIMENTO 		IS NULL			)	AND
				(Y.COD_MATRIZ_CONTABIL 	= @COD_MATRIZ_CONTABIL 	OR @COD_MATRIZ_CONTABIL IS NULL		 	)	AND
				(@APROVACAO_AE				= 'T'							OR CHARINDEX(A.PAGAMENTO_APROVADO,@APROVACAO_AE) > 0)	AND
				(D.COD_FILIAL	= @COD_FILIAL	OR @COD_FILIAL IS NULL)
		GROUP BY (CASE WHEN @VENCIMENTO_REAL = 1 THEN DBO.LX_DATA_REAL(NULL,A.VENCIMENTO_REAL+@D_CP) ELSE A.VENCIMENTO+@D_CP END)
		IF @@ERROR > 0 GOTO ERROR

		INSERT #CTB_FLUXO_CAIXA_TEMP (DATA, CONTAS_A_PAGAR_ATRASO)
		SELECT  (CASE WHEN @VENCIMENTO_REAL = 1 THEN DBO.LX_DATA_REAL(NULL,A.VENCIMENTO_REAL+@D_CP) ELSE A.VENCIMENTO+@D_CP END), 
					SUM(B.VALOR_A_PAGAR_PADRAO)
		FROM CTB_A_PAGAR_PARCELA A
				JOIN W_CTB_A_PAGAR_PARCELA_SALDO B ON
					A.EMPRESA			= B.EMPRESA		AND
					A.LANCAMENTO		= B.LANCAMENTO	AND
					A.ITEM				= B.ITEM			AND
					A.ID_PARCELA		= B.ID_PARCELA
				JOIN CTB_LANCAMENTO C ON
					A.EMPRESA			= C.EMPRESA		AND
					A.LANCAMENTO		= C.LANCAMENTO
            JOIN CTB_A_PAGAR_FATURA D ON
               A.EMPRESA         = D.EMPRESA    AND
               A.LANCAMENTO      = D.LANCAMENTO AND
               A.ITEM            = D.ITEM
				JOIN CTB_LANCAMENTO_ITEM W ON 
					W.EMPRESA			= A.EMPRESA		AND
					W.LANCAMENTO		= A.LANCAMENTO	AND
					W.ITEM				= A.ITEM
				LEFT JOIN #RATEIO_FILIAL_MATRIZ_CONTABIL_TEMP Y ON
					W.RATEIO_FILIAL	= Y.RATEIO_FILIAL
				JOIN #USERS_MATRIZ_CONTABIL_TEMP Z ON
					Y.COD_MATRIZ_CONTABIL			= Z.COD_MATRIZ_CONTABIL
		WHERE (A.EMPRESA = @EMPRESA OR @EMPRESA IS NULL) AND --#01#
				CASE WHEN @VENCIMENTO_REAL = 1 THEN A.VENCIMENTO_REAL ELSE A.VENCIMENTO END 	>= @DATA_INICIAL-(CASE WHEN @ZERA_D <> 0 THEN @D_CP ELSE 0 END)-@DIAS_ATRASO_CP 	AND
				CASE WHEN @VENCIMENTO_REAL = 1 THEN A.VENCIMENTO_REAL ELSE A.VENCIMENTO END 	< @DATA_INICIAL			AND
				A.VALOR_A_PAGAR 				> 0	AND
