				(Y.COD_MATRIZ_CONTABIL 	= @COD_MATRIZ_CONTABIL 	OR @COD_MATRIZ_CONTABIL IS NULL		 	)	AND
				(@APROVACAO_AE				= 'T'							OR CHARINDEX(A.PAGAMENTO_APROVADO,@APROVACAO_AE) > 0)	AND
				(D.COD_FILIAL	= @COD_FILIAL	OR @COD_FILIAL IS NULL)
		GROUP BY (CASE WHEN @VENCIMENTO_REAL = 1 THEN DBO.LX_DATA_REAL(NULL,A.VENCIMENTO_REAL+@D_CP) ELSE A.VENCIMENTO+@D_CP END)
